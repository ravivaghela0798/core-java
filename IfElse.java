/*
 * program to demonstrate the use of if/else statements.
 */

public class IfElse {

    public static void main(String[] args) {
        // Intitialize Integer variable and Assign value to this Variable
        int ageIntValue = 24;

        // Use of if else Condition to varify conditions
        if (ageIntValue >= 18) {
            System.out.println("You are eligible for Vote");
        } else {
            System.out.println("You are not eligble for Vote");
        }
    }

}
